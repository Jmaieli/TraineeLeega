public class Exercicio3 {

    public static void main(String[] args) {

        System.out.println("Usando FOR:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("\nUsando WHILE:");
        int j = 1;
        while (j <= 10) {
            System.out.println(j);
            j++;
        }
    }
}